package bookshop.db;

public class User {
    /**
     * The users's email address.
     *
     * @var String
     */
    public String email;

    /**
     * The users's password used for authenticating.
     *
     * @var String
     */
    public String password;

    /**
     * The users's role to manage permissions.
     *
     * @var String
     */
    public String role;
}
